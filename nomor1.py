bilangan_pertama = int(input('bilangan pertama : '))
bilangan_kedua = int(input('bilangan kedua : '))
hasil_penjumlahan = bilangan_pertama+bilangan_kedua
print(hasil_penjumlahan, 'adalah hasil penjumlahan')
hasil_pengurangan = bilangan_pertama - bilangan_kedua
print(hasil_pengurangan, 'adalah hasil pengurangan')
hasil_modulus = bilangan_pertama//bilangan_kedua
print(hasil_modulus, 'adalah hasil modulus')